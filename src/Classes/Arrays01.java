package Classes;

public class Arrays01 {
    public static void main(String[] args) {
        int[] idades = new int[3];
        idades[0] = 22;
        idades[1] = 20;
        idades[2] = 18;

        System.out.println("idade 1: "+ idades[0]);
        System.out.println("idade 2: "+ idades[1]);
        System.out.println("idade 3: "+ idades[2]);
    }
}
