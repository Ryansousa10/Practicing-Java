package uol.compass.introducao.arrays;

public class Arrays02 {
    public static void main(String[] args) {
        String[] names = new String[3];

        names[0] = "Ryan";
        names[1] = "Sousa";
        names[2] = "Silva";
        for (int i = 0; i < names.length; i++){
            System.out.println(names[i]);
        }




    }
}
